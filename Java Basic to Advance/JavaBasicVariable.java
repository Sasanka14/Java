/*With the help of this code we try to understand the "Variables in Java" */
public class JavaBasicVariable {
    public static void main(String args[]) {
        // Variable declaration and initialization
        int a = 5; // Declares an integer variable 'a' and initializes it with the value 5
        int b = 10; // Declares an integer variable 'b' and initializes it with the value 10
        String name = "Sasanka"; // Declare a String variable 'name' and initializes it with the value Sasanka
        // Output the values of variables 'a' , 'b' and name
        System.out.println("Value of variable a: " + a);
        System.out.println("Value of variable b: " + b);
        System.out.println("Value of variable name: " + name);
    }
}
