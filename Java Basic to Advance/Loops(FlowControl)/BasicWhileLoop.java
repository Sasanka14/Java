// Q: Write a Java program to print the numbers from 1 to 5 using a while loop.

public class BasicWhileLoop {
    public static void main(String[] args) {
        int i = 1; // Initialize the counter variable

        // Start the while loop
        while (i <= 5) { // Condition to run the loop
            System.out.println(i); // Print the current value of i
            i++; // Increment the counter variable
        }
    }
}
