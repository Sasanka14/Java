/* By using "println" and "/n" statement give output*/
public class JavaBasic2 {
    public static void main(String[] args) {
        // Print on different lines using println (automatically adds a newline at the end)
        System.out.println("Hello World!");
        System.out.println("Hello World!");
        System.out.println("Hello World!");

        // Print on different lines using print (manually adding a newline character '\n')
        System.out.print("Hello World!\n"); 
        System.out.print("Hello World!\n"); 
        System.out.print("Hello World!\n"); 
    }
}
