/* How function works without parameters. */
public class DemoCodeExample1 {

    // Function that prints a default greeting message
    public void printGreeting() {
        System.out.println("Hello, world!");
    }

    public static void main(String[] args) {
        DemoCodeExample1 example = new DemoCodeExample1(); // Create an instance of DemoCodeExample1

        // Call the printGreeting function
        example.printGreeting();
    }
}
