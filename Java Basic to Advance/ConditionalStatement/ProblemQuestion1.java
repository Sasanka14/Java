/* Q1. Print largest of 2 numbers using if-else Statement. 
       A = 1;
       B = 3;
 */
public class ProblemQuestion1 {
    public static void main(String[] args) {
        int A = 1;
        int B = 3;

        if (A > B) {
            System.out.println("A is larger than B");
        } else {
            System.out.println("B is larger than A");
        }
    }
}
